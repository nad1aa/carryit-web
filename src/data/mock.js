export function screenDescription(description) {
  if (!description) return null
  const lower = description.toLowerCase()
  for (const item of prohibitedItems) {
    if (lower.includes(item.keyword)) {
      return item.reason
    }
  }
  return null
}

export function computeBookingPrice(kgBooked, pricePerKg) {
  const totalPrice = kgBooked * pricePerKg
  const platformFee = +(totalPrice * 0.10).toFixed(2)
  const travelerPayout = +(totalPrice - platformFee).toFixed(2)
  return { totalPrice, platformFee, travelerPayout }
}
